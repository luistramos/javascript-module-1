/*Esta funcion suma dos valores y retorna el resultado*/
function add (a,b) {
    return a + b;
}

/*Cadena de string*/
const cadena1 = "Buenos"
const cadena2 = " Dias"
const cadenaTotal = add(cadena1,cadena2)
console.log("String: " + cadenaTotal);

/*Suma de valores*/
const valor1 = 4
const valor2 = 3
const valorTotal = add(valor1,valor2)
console.log("Suma: " + valorTotal);

